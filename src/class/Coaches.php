<?php

class Coaches
{

    private $id_coach;
    private $coach_key;
    private $coach_name;
    private $coach_id;
    private $coach_image;
    private $insert_time;


    }

    /**
     * @return mixed
     */
    public function getIdCoach()
    {
        return $this->id_coach;
    }

    /**
     * @return mixed
     */
    public function getCoachKey()
    {
        return $this->coach_key;
    }

    /**
     * @return mixed
     */
    public function getCoachName()
    {
        return $this->coach_name;
    }

    /**
     * @return mixed
     */
    public function getCoachId()
    {
        return $this->coach_id;
    }

    /**
     * @return mixed
     */
    public function getCoachImage()
    {
        return $this->coach_image;
    }

    /**
     * @return mixed
     */
    public function getInsertTime()
    {
        return $this->insert_time;
    }


}